package org.aarboard.ckeditor;

import java.util.ArrayList;
import java.util.HashMap;
import org.apache.commons.lang.StringEscapeUtils;


/**
 *
 * @author a.schild
 */
public class CKEditor
{
    protected String    _fieldName;
    protected String    _fieldID;
    private int     _rows= -1;
    private int     _cols = -1;
    private final HashMap<String, String> _config= new HashMap<>();
    private final HashMap<String, Boolean> _configBoolean= new HashMap<>();
    private final HashMap<String, String> _toolbarDefinition= new HashMap<>();
    private final ArrayList<String> _templateFiles= new ArrayList<>();
    private String  _style;
    private String  _stylesSet;
    private String  _stylesSetURL;

    protected String    _value;

    public CKEditor(String fieldName, String fieldID)
    {
        _fieldName = fieldName;
        _fieldID= fieldID;
    }

    public void setValue(String value)
    {
        _value= value;
    }

    public String renderField()
    {
        StringBuilder sB= new StringBuilder();
        sB.append("<textarea name=\"");
        sB.append(_fieldName);
        sB.append("\" id=\"");
        sB.append(_fieldID);
        sB.append("\" ");
        if (_rows != -1)
        {
            sB.append("rows=\"");
            sB.append(_rows);
            sB.append("\" ");
        }
        if (_cols != -1)
        {
            sB.append("cols=\"");
            sB.append(_cols);
            sB.append("\" ");
        }
        if (_style != null)
        {
            sB.append("style=\"");
            sB.append(_style);
            sB.append("\" ");
        }
        sB.append(">");
        if (_value != null)
        {
            // Make sure we escape all HTML special char stuff
            sB.append(StringEscapeUtils.escapeHtml(_value));
        }
        sB.append("</textarea>\n");
        sB.append("<script type=\"text/javascript\">\n");
	sB.append("//<![CDATA[\n");
	sB.append("CKEDITOR.replace( '");
        sB.append(_fieldID);
        sB.append("',\n");
        sB.append(" {\n");
        boolean firstKey= true;
        for (String configKey : _config.keySet())
        {
            String value= _config.get(configKey);
            if (!firstKey)
            {
                sB.append(",\n");
            }
            else
            {
                firstKey= false;
            }
            sB.append(" ");
            sB.append(StringEscapeUtils.escapeJavaScript(configKey));
            sB.append(" : '");
            sB.append(StringEscapeUtils.escapeJavaScript(value));
            sB.append("'");
        }
        for (String configKey : _configBoolean.keySet())
        {
            Boolean value= _configBoolean.get(configKey);
            if (!firstKey)
            {
                sB.append(",\n");
            }
            else
            {
                firstKey= false;
            }
            sB.append(" ");
            sB.append(StringEscapeUtils.escapeJavaScript(configKey));
            sB.append(" : ");
            sB.append(value.toString());
        }
        for (String toolbarName : _toolbarDefinition.keySet())
        {
            String value= _toolbarDefinition.get(toolbarName);
            if (!firstKey)
            {
                sB.append(",\n");
            }
            else
            {
                firstKey= false;
            }
            sB.append(" ");
            sB.append(StringEscapeUtils.escapeJavaScript("toolbar_"+toolbarName));
            sB.append(" : ");
            sB.append(value);  // Don't escape this one
        }
        if (!_templateFiles.isEmpty())
        {
            if (!firstKey)
            {
                sB.append(",\n");
            }
            else
            {
                firstKey= false;
            }
            boolean isFirstTemplate= true;
            sB.append("templates_files : [");
            for (String template : _templateFiles)
            {
                if (isFirstTemplate)
                {
                    isFirstTemplate= false;
                }
                else
                {
                    sB.append(",");
                }
                sB.append(" '");
                sB.append(StringEscapeUtils.escapeJavaScript(template));
                sB.append("' ");
            }
            sB.append("]");
        }
        if (_stylesSet != null && !_stylesSet.isEmpty())
        {
            if (!firstKey)
            {
                sB.append(",\n");
            }
            else
            {
                firstKey= false;
            }
            sB.append("stylesSet: ");
            sB.append(_stylesSet);
        }
        if (_stylesSetURL != null && !_stylesSetURL.isEmpty())
        {
            if (!firstKey)
            {
                sB.append(",\n");
            }
            else
            {
                // firstKey= false;
            }
            sB.append("stylesSet: ");
            sB.append(StringEscapeUtils.escapeJavaScript(_stylesSetURL));
        }

        sB.append("});\n");
        sB.append("//]]>\n");
        sB.append("</script>\n");

        return sB.toString();
    }

    /**
     * @param rows the _rows to set
     */
    public void setRows(int rows)
    {
        _rows = rows;
    }

    /**
     * @param cols the _cols to set
     */
    public void setCols(int cols)
    {
        _cols = cols;
    }

    public void setFilebrowserBrowseUrl(String url)
    {
        addConfig("filebrowserBrowseUrl", url);
    }

    public void setFilebrowserImageBrowseUrl(String url)
    {
        addConfig("filebrowserImageBrowseUrl", url);
    }

    public void setFilebrowserFlashBrowseUrl(String url)
    {
        addConfig("filebrowserFlashBrowseUrl", url);
    }

    public void setFilebrowserImageBrowseLinkUrl(String url)
    {
        addConfig("filebrowserImageBrowseLinkUrl", url);
    }

    public void setFilebrowserFlashBrowseLinkUrl(String url)
    {
        addConfig("filebrowserFlashBrowseLinkUrl", url);
    }
    
    public void setFilebrowserLinkBrowseUrl(String url)
    {
        addConfig("filebrowserLinkBrowseUrl", url);
    }

    public void setFilebrowserUploadUrl(String url)
    {
        addConfig("filebrowserUploadUrl", url);
    }

    public void setFilebrowserImageUploadUrl(String url)
    {
        addConfig("filebrowserImageUploadUrl", url);
    }
    
    public void setFilebrowserFlashUploadUrl(String url)
    {
        addConfig("filebrowserFlashUploadUrl", url);
    }

    public void addConfig(String key, String value)
    {
        _config.put(key, value);
    }

    public void setFullPage(boolean fullPage)
    {
        addConfig("fullPage", fullPage);
    }

    public void addConfig(String key, boolean value)
    {
        _configBoolean.put(key, value);
    }

    public void setLanguage(String language)
    {
        addConfig("language", language.toLowerCase());
    }

    /**
     * @return the _style
     */
    public String getStyle() {
        return _style;
    }

    /**
     * The skin to use (Default is Kama, can be office2003 and v2)
     *
     * @param style the _style to set
     */
    public void setStyle(String style)
    {
        _style = style;
    }

    /**
     * Use this toolbar
     * 
     * @param toolbarName
     */
    public void setToolbarName(String toolbarName)
    {
        addConfig("toolbar", toolbarName);
    }

    /**
     * Define a toolbar. You still have to select it with setToolbarName()
     *
     *
     * [ <br>
     *       ['Styles', 'Format'], <br>
     *       ['Bold', 'Italic', '-', 'NumberedList', 'BulletedList', '-', 'Link', '-', 'About'] <br>
     *   ] <br>
     * 
     * @param toolbarName
     * @param toolbar
     */
    public void addToolbar(String toolbarName, String toolbar)
    {
        _toolbarDefinition.put(toolbarName, toolbar);
    }

    public void addTemplateFile(String templateFile)
    {
        _templateFiles.add(templateFile);
    }

    /**
     * @return the _stylesSet
     */
    public String getStylesSet() {
        return _stylesSet;
    }

    /**
     * Used to set the styles inline in the form:
     * 
     * [
     * { name : 'Strong Emphasis', element : 'strong' },
     * { name : 'Emphasis', element : 'em' }, ... ];
     * 
     * @param stylesSet the _stylesSet to set
     */
    public void setStylesSet(String stylesSet) 
    {
        _stylesSet = stylesSet;
    }

    /**
     * @return the _stylesSetURL
     */
    public String getStylesSetURL() {
        return _stylesSetURL;
    }

    /**
     * @param stylesSetURL the _stylesSetURL to set
     */
    public void setStylesSetURL(String stylesSetURL) 
    {
        _stylesSetURL = stylesSetURL;
    }

}
