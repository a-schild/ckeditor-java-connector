package org.aarboard.ckeditor.connector.providers.filesystem;

import com.vaadin.terminal.ExternalResource;
import com.vaadin.terminal.Resource;
import com.vaadin.terminal.ThemeResource;
import java.io.File;
import java.io.Serializable;
import java.util.HashMap;
import org.aarboard.ckeditor.connector.providers.IFolder;
import org.aarboard.ckeditor.connector.providers.IIconProvider;
import org.aarboard.ckeditor.connector.providers.IObject;
import org.aarboard.ckeditor.connector.providers.IThumbnailProvider;

/**
 *
 * @author a.schild
 */
public class FileObject implements IObject, IIconProvider, IThumbnailProvider, Serializable
{
    private static final long serialVersionUID = 1L;

    protected IFolder _parent;
    protected File _mySelf;
    protected boolean _isFileBrowser;
    protected String _images[] = {"jpg", "jpeg", "bmp", "tif", "tiff", "gif", "png" };
    protected String _icons[] = {"aac",
        "ai",
        "aiff",
        "avi",
        "bmp",
        "c",
        "cpp",
        "css",
        "dat",
        "dmg",
        "doc",
        "dotx",
        "dwg",
        "dxf",
        "eps",
        "exe",
        "flv",
        "gif",
        "h",
        "hpp",
        "html",
        "ics",
        "iso",
        "java",
        "jpg",
        "key",
        "mid",
        "mp3",
        "mp4",
        "mpg",
        "odf",
        "ods",
        "odt",
        "otp",
        "ots",
        "ott",
        "pdf",
        "php",
        "png",
        "ppt",
        "psd",
        "py",
        "qt",
        "rar",
        "rb",
        "rtf",
        "sql",
        "tga",
        "tgz",
        "tiff",
        "txt",
        "wav",
        "xls",
        "xlsx",
        "xml",
        "yml",
        "zip"};
    private final HashMap<String, String> _iconSubstitutes= new HashMap<>();

    public FileObject(File mySelf, IFolder parent, boolean isFileBrowser) {
        _parent = parent;
        _mySelf = mySelf;
        _isFileBrowser = isFileBrowser;
        _iconSubstitutes.put("jpeg", "jpg");
        _iconSubstitutes.put("tiff", "tif");
    }

    @Override
    public String getName() {
        return _mySelf.getName();
    }

    @Override
    public String getURL() {
        String retVal = null;
        String rootPath= ((Folder)_parent).getFsProvider().getRootPath();
        String rootURL= ((Folder)_parent).getFsProvider().getRootURL();
        String filePath = _mySelf.getAbsolutePath();
        if (filePath.startsWith(rootPath)) 
        {
            String remainingPart = filePath.substring(rootPath.length());
            if (remainingPart.startsWith("/") || remainingPart.startsWith("\\"))
            {
                retVal = rootURL + remainingPart.substring(1).replace('\\', '/'); // MS Windows fix
            }
            else
            {
                retVal = rootURL + remainingPart.replace('\\', '/'); // MS Windows fix
            }
        } else {
            retVal = filePath;
        }
        return retVal;
    }

    @Override
    public boolean isRenameAllowed() {
        return true;
    }

    @Override
    public boolean isDeleteAllowed() {
        return true;
    }

    @Override
    public boolean isResizeAllowed() {
        return false;
    }

    @Override
    public boolean delete() {
        return _mySelf.delete();
    }

    @Override
    public boolean handleRename(String newName) {
        File newFile = new File(_mySelf.getParent() + File.separator + newName);
        boolean retVal = _mySelf.renameTo(newFile);
        if (retVal) {
            _mySelf = newFile;
        } else {
            // Failed
        }
        return retVal;
    }

    /**
     * @return True if this item can be selected
     */
    @Override
    public boolean isSelectAllowed() {
        return true;
    }

    @Override
    public String getListIcon() {
        String fName = _mySelf.getName();
        int lastDot = fName.lastIndexOf('.') + 1;
        if (lastDot != -1) {
            String ext = fName.substring(lastDot).toLowerCase();
            // Find match of icons
            for (String icon : _icons)
            {
                if (icon.equals(ext))
                {
                    return ext;
                }
            }
            // Not found, search in alias table
            ext= _iconSubstitutes.get(ext);
            if (ext == null)
            {
                // Fall back to blank default
                ext= "_blank";
            }
            return ext;
        } else {
            // Sorry, no file extension, no file type, we don't inspect 
            // the content of the file to guess the type
            return null;
        }
    }

    /**
     * 
     * @param width     Width of thumbnail, can be -1 if it should be scaled 
     * @param height    Height of thumbnail, can be -1 if it should be scaled 
     * @return 
     */
    @Override
    public Resource getThumbnail(int width, int height) 
    {
        String fName = _mySelf.getName();
        int lastDot = fName.lastIndexOf('.') + 1;
        if (lastDot != -1) 
        {
            String ext = fName.substring(lastDot).toLowerCase();
            String imgType= null;
            for (String img : _images)
            {
                if (img.equalsIgnoreCase(ext))
                {
                    imgType= img;
                }
            }
            if (imgType != null)
            {
                ExternalResource rsc= new ExternalResource(getURL());
                return rsc;
            }
            else
            {
                String lIcon= getListIcon();
                if (lIcon != null)
                {
                    String iconSize= "48px";
                    int iiconSize= height != -1 ? height : width;
                    if (iiconSize != -1)
                    {
                        if (iiconSize <= 16)
                        {
                            iconSize= "16px";
                        }
                        else if (iiconSize <= 32)
                        {
                            iconSize= "32px";
                        }
                        else if (iiconSize <= 48)
                        {
                            iconSize= "48px";
                        }
                        else
                        {
                            iconSize= "512px";
                        }
                    }
                    ThemeResource thRsc= new ThemeResource("../../icons/"+iconSize+"/"+lIcon+".png");
                    return thRsc;
                }
                else
                {
                    return null;
                }
            }
        } 
        else 
        {
            // Sorry, no file extension, no file type, we don't inspect 
            // the content of the file to guess the type
            return null;
        }        
    }
}
